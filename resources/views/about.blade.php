				<li>Exposure — Girls Who Code Clubs host speakers, coordinate field trips and engage with the local tech community. Girls Who Code facilitates exclusive connections where possible, providing unique opportunities to Club participants.</li>
				<li>Community — As Clubs participants, girls have access to this supportive and engaged network of teachers, mentors, professionals, and fellow students.</li>
			</ol>
