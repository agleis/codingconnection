    	<p>The Cornell chapter of Girls Who Code is launching in December of 2015, pairing with Women in Computing Cornell, a student organization dedicated to expanding the community of women computer scientists and to sustain a supportive network in the field. The Girls Who Code club teaches girls in grades 6 through 12 essential computer science concepts, provide mentorship, and a community of peers with similar interests. The larger network of Girls Who Code has served 3,860 girls in 29 states.</p>
